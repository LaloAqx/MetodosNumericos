    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        fila = Integer.parseInt(n.getText());
                
        columna = fila;
        
        Object col[] = new Object[columna];//  al erreglo mostrara en el titulo del JTabel las las incognitas y su solucion de la ecuaciom
            // ejemplo si el sistema tiene 2 incognitas aparecera en la pantalla una matriz de 2x3
            // se motrara en el titulo del JTable  x1  x2   d
            for (int j = 0; j < columna; j++) {
                if (j < columna ) {
                    col[j] = "X" + (j + 1);
                } 
            }
            modelo = new DefaultTableModel(col, fila);// se muestra el titulo y el Jtablet toma la dimension de la matriz aumentada
            jTable1.setModel(modelo); 
            
        //int n = Integer.parseInt(jTextFieldNumeroDeIncognitas.getText());// alamacena el nuemro de ingonitas ingresado por teclado

            
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       fila = Integer.parseInt(n.getText());
                
        columna = fila;
        double m[][] = new double[fila][columna];// almacena los coeficientes de la matriz aumentada
            
            for (int i = 0; i < fila; i++) {// pasamos a alamcenar en un arreglo los datos ingresados en el JTable
                for (int j = 0; j < columna; j++) {//
                    m[i][j] = Double.parseDouble(String.valueOf(jTable1.getValueAt(i, j)));
                }
                
            }
         String mensaje = Double.toString(Determinante.determinanteMatriz(m, fila));
         resultado.setText(mensaje); 
        
            
    }//GEN-LAST:event_jButton2ActionPerformed

